<h3>El peligro del arsénico en el agua y la tecnología para acabar con él</h3><br>

<img class="imagecontent" alt=">El peligro del arsénico en el agua y la tecnología para acabar con él" width="400" src="images/aguanews.jpg" />

<p>
En las últimas dos décadas, en México, se ha consumido el doble del arsénico permitido por la OMS. 
</p>
<p>
El arsénico es una sustancia que predispone al desarrollo de enfermedades como el cáncer de pulmón, piel, pecho, próstata, riñones e hígado, así como lesiones cutáneas y el desarrollo de enfermedades cardiovasculares y diabetes.
</p>
<p>
De acuerdo con un análisis de impactos y evaluación, elaborado por la Comisión Federal para la Protección contra Riesgos Sanitarios (Cofepris), en la actualidad la legislación mexicana establece un límite máximo permisible de arsénico en el agua para uso y consumo humano de 0.025 miligramos por litro (mg/L), cuando desde hace más de veinte años la OMS estableció este límite en 0.01 mg/L.
</p>
<p>
El arsénico es una de las 10 sustancias químicas más peligrosas para la salud pública, y México es uno de los países que presentan altos niveles de este tóxico inorgánico en mantos acuíferos subterráneos, de donde proviene la mayor parte del agua para consumo humano, ya que se calcula que de cada 329.000 litros por segundo que se suministran, el 62% proviene de fuentes subterráneas. El anteproyecto de ley NOM-250-SSA1-2014, para el cual se han llevado a cabo estos estudios, pretende renovar el proceso para la eliminación de arsénico en el agua, lo que conlleva el mejoramiento de las diferentes plantas potabilizadoras en todo el país que no cumplan los requisitos de la nueva ley y la inversión en nuevos equipos de tratamiento.
</p>
<p>
AZUD ha desarrollado una tecnología que salva vidas: las plantas potabilizadoras AZUD DW DU son la solución perfecta para la eliminación de arsénico en las condiciones más adversas. Concretamente en México, las plantas AZUD DW DU suministran agua potable a poblaciones aisladas que hasta la fecha se abastecían con agua contaminada por arsénico. 
</p>

