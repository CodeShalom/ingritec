 	<li>SOTRAFA, S.A., fabricante de láminas de polietileno, plásticos para uso agrícola, ganadero, construcción, e impermeabilización.</li>
 	<li>Teyme fabricantes de los equipos para fumigación agrícola. </li>
         </ul>
