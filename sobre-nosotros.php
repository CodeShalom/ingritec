<h2 class="titles in">Sobre <span>nosotros</span></h2>

<img class="imagecontent" width="366" alt="Sobre nosotros" height="178" src="images/fotoempresa.jpg" />

<p>
INGRITEC fue fundada en el año 1992 con la finalidad ofrecer servicios en el ámbito agropecuario para cubrir un vacío tecnológico relacionado con el desarrollo de Sistema de Riego Presurizados (Goteo, Microaspersión, Aspersión, Pivote Central, Ventanillas Regulables).
</p>
<p>
Con la finalidad de apoyo al desarrollo de estos sistemas nos asociamos a varios ingenieros con especialidades diferentes; suelos y riego, obras hidráulicas, planificación de recursos hidráulicos y electromecánica.
</p>
<p>
Tomando en consideración que en el país no había suficiente experiencia en los sistemas mencionados más arriba, nos propusimos buscar alianzas estratégicas con empresas internacionales especializadas en el diseño, suministro e instalación de Sistemas de Riego. Y fue así, como en el año 1992 INGRITEC hizo un consorcio con Ura Riego, para la instalación del primer Sistema de Riego por Aspersión importante, de unas 3,500 tareas, utilizando el desnivel del terreno para generar la presión del Sistema de Riego, el cual se alimentaba del Canal Nizaíto, en la zona de Nizaíto, Enriquillo. Lo que constituyó a elevar nuestra experiencia en la ejecución de este tipo de proyectos, ya que en ese momento Ura Riego era una empresa líder en el diseño, suministro e instalación de Sistemas de Riego Presurizado en España.  
</p>
<p>
A partir del año 1992 las exigencias del mercado nos indujeron a incursionar en la venta e instalación de materiales de riego como cintas de riego por goteo, mangueras de polietileno, sarán, plásticos para invernaderos, plásticos para control de malezas y geomembrana para revestir reservorios de agua, así como también, equipos hidroneumáticos para la aplicación de fungicidas, insecticidas, fertilizantes foliares e invernaderos. 
</p>
<p>
<strong>Para apoyar estas actividades hemos establecido relaciones comerciales con: </strong>
</p>
